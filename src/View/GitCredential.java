    private void jSlider1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSlider1StateChanged
        Font f = jLabel2.getFont();
        if (jSlider1.getValue() == 1) {
            jLabel2.setFont(f.deriveFont(f.getStyle() | Font.BOLD));
            jLabel4.setFont(f.deriveFont(f.getStyle() | Font.BOLD));
            jLabel3.setFont(f.deriveFont(f.getStyle() & ~Font.BOLD));
            jLabel5.setFont(f.deriveFont(f.getStyle() & ~Font.BOLD));
            persistance = false;
        } else {
            jLabel3.setFont(f.deriveFont(f.getStyle() | Font.BOLD));
            jLabel5.setFont(f.deriveFont(f.getStyle() | Font.BOLD));
            jLabel2.setFont(f.deriveFont(f.getStyle() & ~Font.BOLD));
            jLabel4.setFont(f.deriveFont(f.getStyle() & ~Font.BOLD));
            persistance = true;
        }

    }//GEN-LAST:event_jSlider1StateChanged

